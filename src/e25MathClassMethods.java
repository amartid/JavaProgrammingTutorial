public class e25MathClassMethods {

    public static void main(String[] args) {
        System.out.println(Math.abs(-26.7)); //absolute value
        System.out.println(Math.ceil(7.4)); //roundup
        System.out.println(Math.floor(7.4)); //rounddown
        System.out.println(Math.max(7.4, 26.7)); //maximum
        System.out.println(Math.min(7.4, 26.7)); //minimum
        System.out.println(Math.pow(5, 3)); //minimum
        System.out.println(Math.sqrt(9)); //sqrt

    }
}